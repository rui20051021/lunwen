    CASE (n % 4)
        WHEN 0 THEN ROUND(2 + (RAND() * 6), 2)      -- 温度
        WHEN 1 THEN ROUND(80 + (RAND() * 15), 2)    -- 湿度
        WHEN 2 THEN ROUND(30 + (RAND() * 40), 2)    -- GPS速度
        ELSE ROUND(RAND(), 0)                        -- 门状态 0/1
    END,
    CASE (n % 4)
        WHEN 0 THEN '℃'
        WHEN 1 THEN '%'
        WHEN 2 THEN 'km/h'
        ELSE 'status'
    END,
    116.397428 + (RAND() * 5),  -- 经度
    39.90923 + (RAND() * 3),     -- 纬度
    FLOOR(60 + RAND() * 40),     -- 信号强度 60-100
