    'good',
    DATE_SUB(NOW(), INTERVAL FLOOR(RAND() * 72) HOUR),  -- 最近3天
    NOW()
FROM vehicles v
CROSS JOIN transports t
CROSS JOIN (
    SELECT 1 AS n UNION ALL SELECT 2 UNION ALL SELECT 3 UNION ALL 
